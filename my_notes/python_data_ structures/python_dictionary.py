'''             What is a Python dictionary
A dictionary is a collection of key value pairs.
The values can be changed(mutable).
The values have unique keys, so I can have duplicate keys for the same values.
I can store mixed data types.



Differient ways to create a Dictionaries
'''
mycar = {
    "brand": "Range Rover Sports",
    "model": "HSE",
    "year": 2017
}

print(mycar)  # {'brand': 'Range Rover Sports', 'model': 'HSE', 'year': 2017}









''' Using a constructor to create a dictionary
A constructor is a special function I can use to create an object.



'''
mygreens = dict(fruit="green apples", vegetables="kale")

print(mygreens)  # {'fruit': 'green apples', 'vegetables': 'kale'}

